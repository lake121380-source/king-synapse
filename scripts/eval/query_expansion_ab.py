"""Query expansion A/B: test HyDE vs category expansion on no-rank samples.

For each of the 174 no-rank samples, generate three query variants:
1. original: the raw question
2. hyde: a hypothetical answer generated by DeepSeek
3. category_expansion: hypernym/hyponym variants generated by DeepSeek

Then compute cosine similarity between each variant's embedding and the
relevant chunk's embedding using the same multilingual-e5-base model.
Report which variant brings the relevant chunk closest.

This is a lightweight proxy for retrieval improvement: if the cosine
similarity between the expanded query and the relevant chunk increases
significantly, the expanded query would likely retrieve that chunk.
"""

