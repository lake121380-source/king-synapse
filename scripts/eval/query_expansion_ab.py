import json
import os
import re
import sys
import time
import urllib.error
import urllib.request
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import numpy as np

sys.path.insert(0, str(Path(__file__).parent))

from longmem_dmr_smoke import (
    DMR_FILE,
    DMR_REPO,
    build_dialog_chunk,
    default_cache_root,
    default_fastembed_cache_dir,
    dmr_answer_matches,
    download_dataset,
    read_jsonl,
    repo_root,
    stable_hash,
)

from official_dmr_eval import build_official_dmr_dataset


def generate_variant(
    *,
    base_url: str,
    model: str,
    question: str,
    variant_type: str,
) -> str:
    """Generate a query expansion variant using DeepSeek."""
    api_key = os.environ.get("DEEPSEEK_API_KEY")
    if not api_key:
        return ""

    if variant_type == "hyde":
        prompt = (
            f"Answer this question in one or two sentences as if you are the person being asked. "
            f"Use specific, concrete language. Even if you're guessing, give a confident answer.\n\n"
            f"Question: {question}\n\n"
            f"Answer:"
        )
    elif variant_type == "category_expansion":
        prompt = (
            f"Given this question, generate a list of specific instances and related terms "
            f"that the answer might be about. Include both the category (hypernym) and "
            f"possible specific instances (hyponyms).\n\n"
            f"Question: {question}\n\n"
            f"Return a comma-separated list of terms (no explanation):"
        )
    else:
        return question

    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": "You are a helpful assistant. Be concise."},
            {"role": "user", "content": prompt},
        ],
        "temperature": 0,
        "max_tokens": 100,
        "thinking": {"type": "disabled"},
    }
    data = json.dumps(payload).encode("utf-8")
    request = urllib.request.Request(
        base_url.rstrip("/") + "/chat/completions",
        data=data,
        headers={
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
        },
        method="POST",
    )

    try:
        with urllib.request.urlopen(request, timeout=30) as response:
            body = json.loads(response.read().decode("utf-8", errors="replace"))
        content = body["choices"][0]["message"].get("content", "").strip()
        if not content:
            content = body["choices"][0]["message"].get("reasoning_content", "").strip()
        return content or ""
    except Exception:
        return ""


def get_embeddings(texts: list[str], embedder) -> list[np.ndarray]:
    """Get embeddings for a list of texts."""
    if not texts:
        return []
    vecs = list(embedder.embed(texts))
    return [np.array(v) for v in vecs]


def cosine_sim(a: np.ndarray, b: np.ndarray) -> float:
    """Cosine similarity between two vectors."""
    na = np.linalg.norm(a)
    nb = np.linalg.norm(b)
    if na == 0 or nb == 0:
        return 0.0
    return float(np.dot(a, b) / (na * nb))


def main() -> int:
    import argparse

    parser = argparse.ArgumentParser(description="Query expansion A/B on no-rank samples.")
    parser.add_argument("--classification-report", type=Path,
                        default=repo_root() / "crates/eval/reports/dmr-no-rank-failure-classification.json")
    parser.add_argument("--synth-report", type=Path,
                        default=repo_root() / "crates/eval/reports/official-dmr-500-deepseek-synthesize.json")
    parser.add_argument("--output", type=Path,
                        default=repo_root() / "crates/eval/reports/query-expansion-ab.json")
    parser.add_argument("--model", default="deepseek-v4-flash")
    parser.add_argument("--base-url", default=os.environ.get("DEEPSEEK_BASE_URL", "https://api.deepseek.com"))
    parser.add_argument("--dmr-answer-match", default="significant_token_containment")
    parser.add_argument("--endpoint", default=os.environ.get("HF_ENDPOINT", "https://huggingface.co"))
    parser.add_argument("--cache-root", type=Path, default=default_cache_root())
    parser.add_argument("--fastembed-cache-dir", type=Path, default=default_fastembed_cache_dir())
    parser.add_argument("--limit", type=int, default=None)
    args = parser.parse_args()

    os.environ["HF_ENDPOINT"] = args.endpoint
    os.environ["FASTEMBED_CACHE_DIR"] = str(args.fastembed_cache_dir)
    os.environ["HF_HUB_OFFLINE"] = "1"

    # Load classification to get no-rank sample IDs and their categories
    cls_report = json.loads(args.classification_report.read_text(encoding="utf-8"))
    cls_by_id = {c["sample_id"]: c["classification"]["category"] for c in cls_report["classifications"]}

    # Load synth report to get sample IDs
    synth = json.loads(args.synth_report.read_text(encoding="utf-8"))
    no_rank_ids = {
        item["sample_id"] for item in synth["answer_generation"]["per_query"]
        if item["first_relevant_rank"] is None
    }

    # Load DMR dataset
    dmr_cache = args.cache_root / "dmr-msc-self-instruct"
    dmr_path = download_dataset(DMR_REPO, DMR_FILE, args.endpoint, dmr_cache)
    rows = read_jsonl(dmr_path)
    memories, queries, examples, skipped = build_official_dmr_dataset(
        rows, 500, args.dmr_answer_match
    )
    memory_by_key = {m["key"]: m["content"] for m in memories}

    # Load embedding model directly via onnxruntime (same model as system uses)
    print("Loading embedding model (multilingual-e5-base via onnxruntime)...")
    import onnxruntime as ort
    from tokenizers import Tokenizer

    model_base = args.fastembed_cache_dir / "models--intfloat--multilingual-e5-base"
    snapshot_dir = model_base / "snapshots"
    # Find the snapshot directory
    snap = list(snapshot_dir.iterdir())[0]
    # Resolve symlinks to actual blob files
    model_onnx = (snap / "onnx" / "model.onnx").resolve()
    tokenizer_path = (snap / "tokenizer.json").resolve()

    # Set up CUDA DLL path for onnxruntime
    cuda_runtime = Path(os.environ.get("LOCALAPPDATA", "")) / "king-synapse" / "cuda-runtime-py313"
    if cuda_runtime.exists():
        os.environ["PATH"] = str(cuda_runtime) + os.pathsep + os.environ.get("PATH", "")

    sess_options = ort.SessionOptions()
    sess_options.graph_optimization_level = ort.GraphOptimizationLevel.ORT_ENABLE_ALL
    session = ort.InferenceSession(
        str(model_onnx),
        sess_options,
        providers=["CUDAExecutionProvider", "CPUExecutionProvider"],
    )
    tokenizer = Tokenizer.from_file(str(tokenizer_path))
    tokenizer.enable_padding(length=256)
    tokenizer.enable_truncation(max_length=256)
    print(f"Embedding model loaded. Providers: {session.get_providers()}")

    def embed_texts(texts):
        if not texts:
            return []
        # E5 requires "query: " prefix for queries, no prefix for passages
        encoded = [tokenizer.encode(t) for t in texts]
        input_ids = np.array([e.ids for e in encoded], dtype=np.int64)
        attention_mask = np.array([e.attention_mask for e in encoded], dtype=np.int64)
        outputs = session.run(None, {
            "input_ids": input_ids,
            "attention_mask": attention_mask,
        })
        # E5 uses mean pooling or CLS token depending on config
        # The model output[0] is last_hidden_state: [batch, seq_len, hidden]
        hidden = outputs[0]
        # Mean pooling
        mask_expanded = np.expand_dims(attention_mask, -1).astype(hidden.dtype)
        summed = np.sum(hidden * mask_expanded, axis=1)
        counts = np.clip(np.sum(attention_mask, axis=1, keepdims=True), 1, None).astype(hidden.dtype)
        embeddings = summed / counts
        # Normalize
        norms = np.linalg.norm(embeddings, axis=1, keepdims=True)
        norms = np.clip(norms, 1e-12, None)
        embeddings = embeddings / norms
        return [embeddings[i] for i in range(len(texts))]

    # Warm up
    embed_texts(["warmup"])
    print("Embedding model ready.")

    results = []
    started = time.perf_counter()
    category_stats = {}  # category -> list of (orig_sim, hyde_sim, cat_sim)

    for idx, (query, example) in enumerate(zip(queries, examples)):
        if example["sample_id"] not in no_rank_ids:
            continue
        if args.limit and len(results) >= args.limit:
            break

        sid = example["sample_id"]
        category = cls_by_id.get(sid, "unknown")
        question = query["query"]
        relevant_keys = query["relevant"]

        # Get relevant chunk content
        relevant_contents = [memory_by_key[k] for k in relevant_keys if k in memory_by_key]
        if not relevant_contents:
            continue

        # Use the first relevant chunk as target
        target_chunk = relevant_contents[0]
        # Truncate to embedding model's practical limit
        target_text = target_chunk[:512]

        # Generate variants
        hyde_text = generate_variant(
            base_url=args.base_url, model=args.model,
            question=question, variant_type="hyde"
        )
        cat_text = generate_variant(
            base_url=args.base_url, model=args.model,
            question=question, variant_type="category_expansion"
        )

        # Embed all: original query, hyde, category, target chunk
        texts_to_embed = [question, hyde_text, cat_text, target_text]
        embeddings = embed_texts(texts_to_embed)
        if len(embeddings) < 4:
            continue

        orig_sim = cosine_sim(embeddings[0], embeddings[3])
        hyde_sim = cosine_sim(embeddings[1], embeddings[3])
        cat_sim = cosine_sim(embeddings[2], embeddings[3])

        result = {
            "sample_id": sid,
            "category": category,
            "orig_sim": round(orig_sim, 4),
            "hyde_sim": round(hyde_sim, 4),
            "cat_sim": round(cat_sim, 4),
            "hyde_delta": round(hyde_sim - orig_sim, 4),
            "cat_delta": round(cat_sim - orig_sim, 4),
            "hyde_better": hyde_sim > orig_sim,
            "cat_better": cat_sim > orig_sim,
        }
        results.append(result)

        if category not in category_stats:
            category_stats[category] = []
        category_stats[category].append((orig_sim, hyde_sim, cat_sim))

        if (len(results) % 20) == 0:
            elapsed = time.perf_counter() - started
            print(f"  processed {len(results)}/{len(no_rank_ids)} ({elapsed:.0f}s)")

    elapsed_ms = (time.perf_counter() - started) * 1000

    # Aggregate by category
    agg = {}
    for cat, sims in category_stats.items():
        n = len(sims)
        origs = [s[0] for s in sims]
        hydes = [s[1] for s in sims]
        cats = [s[2] for s in sims]
        agg[cat] = {
            "n": n,
            "orig_sim_mean": round(sum(origs) / n, 4),
            "hyde_sim_mean": round(sum(hydes) / n, 4),
            "cat_sim_mean": round(sum(cats) / n, 4),
            "hyde_delta_mean": round((sum(hydes) - sum(origs)) / n, 4),
            "cat_delta_mean": round((sum(cats) - sum(origs)) / n, 4),
            "hyde_better_count": sum(1 for o, h, c in sims if h > o),
            "cat_better_count": sum(1 for o, h, c in sims if c > o),
            "hyde_better_rate": round(sum(1 for o, h, c in sims if h > o) / n, 4),
            "cat_better_rate": round(sum(1 for o, h, c in sims if c > o) / n, 4),
        }

    output = {
        "schema_version": "king-synapse.query-expansion-ab.v1",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "runner": "scripts/eval/query_expansion_ab.py",
        "model": args.model,
        "embedding_model": "intfloat/multilingual-e5-base (same as system, via onnxruntime)",
        "total_samples": len(results),
        "elapsed_ms": round(elapsed_ms),
        "aggregate_by_category": agg,
        "raw_questions_committed": False,
        "raw_answers_committed": False,
        "raw_dialogs_committed": False,
        "results": results,
    }

    args.output.parent.mkdir(parents=True, exist_ok=True)
    args.output.write_text(json.dumps(output, indent=2, ensure_ascii=False), encoding="utf-8")

    print(f"\nDone. {len(results)} samples in {elapsed_ms:.0f}ms")
    print("\nAggregate by category:")
    for cat, a in sorted(agg.items()):
        print(f"  {cat} (n={a['n']}):")
        print(f"    orig={a['orig_sim_mean']} hyde={a['hyde_sim_mean']} (delta={a['hyde_delta_mean']}, better={a['hyde_better_rate']})")
        print(f"    orig={a['orig_sim_mean']} cat={a['cat_sim_mean']} (delta={a['cat_delta_mean']}, better={a['cat_better_rate']})")
    print(f"\nOutput: {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
